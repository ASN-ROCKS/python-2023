# %%
print("Operações de SOMA:")
print("4 + 4 =", 4 + 4)
print("2 + 2 =", 2 + 2)
print("10 + 10 =", 10 + 10)

# %%
print("Operações de SUBTRAÇÃO:")
print("4 - 4 =", 4 - 4)
print("24 - 2 =", 24 - 2)
print("87 - 10 =", 87 - 10)

# %%
print("Operações de MULTIPLICAÇÃO:")
print("4 * 4 =", 4 * 4)
print("24 * 2 =", 24 * 2)
print("87 * 10 =", 87 * 10)

# %%
print("Operações de DIVISÃO (float):")
print("4 / 4 =", 4 / 4)
print("24 / 2 =", 24 / 2)
print("87 / 10 =", 87 / 10)

# %%
print("Operações de DIVISÃO (int):")
print("4 // 4 =", 4 // 4)
print("24 // 2 =", 24 // 2)
print("87 // 10 =", 87 // 10)

# %%
print("Operações de DIVISÃO (resto):")
print("4 % 4 =", 4 % 4)
print("24 % 2 =", 24 % 2)
print("87 % 10 =", 87 % 10)

# %%
print("Operações de POTÊNCIA:")
print("4 ** 4 =", 4 ** 4)
print("24 ** 2 =", 24 ** 2)
print("87 ** 10 =", 87 ** 10)

# %%
print("Operações de RAIZ QUADRADA:")
print("4 ** 0.5 =", 4 ** 0.5)
print("4 ** (0.5) =", 4 ** (0.5))
print("24 ** (1/2) =", 24 ** (1/2))
print("24 ** (1/2) =", 24 ** (1/2))
print("87 ** (1/2) =", 87 ** (1/2))
